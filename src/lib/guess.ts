export function guessCategory(name: string): Category {
  const n = name.replace(/\s|　/g, "");
  for (const [cat, kws] of CATEGORY_KEYWORDS) {
    if (kws.some((k) => n.includes(k))) return cat;
  }
  return "その他";
}

