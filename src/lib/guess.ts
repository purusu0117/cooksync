// カテゴリ別の既定日数（個別キーワードに当たらなかった場合）
const CATEGORY_DEFAULT_DAYS: Record<Category, number> = {
  "肉・魚": 3,
  野菜: 7,
  "乳製品・卵": 7,
  主食: 14,
  調味料: 180,
  飲料: 30,
  その他: 7,
};

export function guessShelfLifeDays(name: string, category?: Category): number {
  const n = name.replace(/\s|　/g, "");
  for (const [kws, days] of ITEM_SHELF_DAYS) {
    if (kws.some((k) => n.includes(k))) return days;
  }
  return CATEGORY_DEFAULT_DAYS[category ?? guessCategory(name)];
}

export interface Guess {
  category: Category;
  expiresOn: string;
  shelfLifeDays: number;
}

/** 食材名と購入日から、カテゴリと賞味期限の目安を推測する */
export function guessItem(name: string, purchasedOn: string): Guess {
  const category = guessCategory(name);
  const shelfLifeDays = guessShelfLifeDays(name, category);
  return {
    category,
    shelfLifeDays,
    expiresOn: estimateExpiry(purchasedOn, shelfLifeDays),
  };
}
