const TIPS = new Map<string, string>(
  RAW_TIPS.map(([k, v]) => [normalizeName(k), v]),
);

/** レシピの保存リスト（明示の leftoverStorage ＋ 保存メモが無い材料の自動補完） */
export function storageList(
  recipe: Recipe,
): { ingredient: string; method: string; auto?: boolean }[] {
  const out: { ingredient: string; method: string; auto?: boolean }[] =
    recipe.leftoverStorage.map((l) => ({ ...l }));
  for (const ing of recipe.ingredients) {
    if (ing.basicSeasoning) continue;
    if (out.some((o) => ingredientMatches(o.ingredient, ing.name))) continue;
    const tip = TIPS.get(normalizeName(ing.name));
    if (tip) out.push({ ingredient: ing.name, method: tip, auto: true });
  }
  return out;
}
